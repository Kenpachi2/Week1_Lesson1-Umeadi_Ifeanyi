
let name = "Kenpachi";
let nameString = "Hello, " + name + "!";

console.log(nameString); //Node Output: Hello, Kenpachi!

let upperCaseString = nameString.toUpperCase();
console.log(upperCaseString); //Node Output: HELLO, KENPACHI!

let lowerCaseString = nameString.toLowerCase();
console.log(lowerCaseString); //Node Output: hello, kenpachi!

let slicedString = nameString.slice(7);
console.log(slicedString); //Node Output: Kenpachi!
