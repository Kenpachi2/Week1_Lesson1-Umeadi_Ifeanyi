
//Declaring a variable without assigning a value to it

let undefinedVariable;
console.log(undefinedVariable); //Nodemon Output: undefined
console.log(typeof undefinedVariable); //Nodemon Output: "undefined"

//Assigning null to a variable and checking its type using typeof
let nullVariable = null;
console.log(nullVariable); //Nodemon Output: null
console.log(typeof nullVariable); //Nodemon Output: "object"
